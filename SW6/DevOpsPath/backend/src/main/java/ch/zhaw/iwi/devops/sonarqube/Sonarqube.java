package ch.zhaw.iwi.devops.sonarqube;

public class Sonarqube {

    public static final int convert(int number) {
            if (number == 1) return 1;
            else if (number == 2) return 2;
            else if (number == 3) return 3;
            else if (number == 4) return 4;
            else return 5;


    }
    
}
